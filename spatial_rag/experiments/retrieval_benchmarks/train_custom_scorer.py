import os
import sys
import json
import glob
import torch
import torch.nn.functional as F
import torch.optim as optim
from tqdm import tqdm

sys.path.append(os.path.abspath(os.path.join(os.path.dirname(__file__), '../../..')))
sys.path.append(os.path.abspath(os.path.join(os.path.dirname(__file__), '../..')))

from spatial_rag.embedder import Embedder
from spatial_rag.query_frame_scorer import set_ce_loss, FrameQueryScorer

def main():
    device = torch.device('mps' if torch.backends.mps.is_available() else 'cuda' if torch.cuda.is_available() else 'cpu')
    
    # 1. Load benchmark JSON
    benchmark_file = os.path.join(os.path.dirname(__file__), "autogenerated_benchmark_20.json")
    with open(benchmark_file, "r") as f:
        benchmark_data = json.load(f)
        
    print(f"Loaded {len(benchmark_data)} queries for training.")
    
    # 2. Gather Images
    img_dir = os.path.join(os.path.dirname(__file__), "../../../captured_images")
    image_paths = sorted(glob.glob(os.path.join(os.path.abspath(img_dir), '*.jpg')))[:50]
    
    # 3. Initialize Shared Embedder
    print(f"\n[System] Loading core Embedder (CLIP)...")
    embedder = Embedder()
    
    # 4. Precompute Image Embeddings
    print("Precomputing Image Embeddings...")
    from PIL import Image
    imgs = [Image.open(p).convert("RGB") for p in image_paths]
    img_tensors = torch.stack([embedder.preprocess(img) for img in imgs]).to(embedder.device)
    embs = []
    with torch.no_grad():
        for i in range(0, img_tensors.size(0), 16):
            batch_emb = embedder.model.encode_image(img_tensors[i:i+16])
            embs.append(batch_emb.cpu())
    img_embs = torch.cat(embs, dim=0) # [50, d]
    
    A_list = []
    B_list = []
    pos_mask_list = []
    valid_mask_list = []
    
    # 5. Build Training dataset
    num_frames = 50
    dim = img_embs.size(-1)
    
    print("Building tensors...")
    for task in tqdm(benchmark_data):
        q_text = task["query_text"]
        gt_frames = set(task["gt_frames"])
        
        # embed query
        q_tensor = embedder.tokenizer(q_text).to(embedder.device)
        with torch.no_grad():
            q_emb = embedder.model.encode_text(q_tensor).cpu().squeeze(0) # [d]
            
        p_mask = torch.zeros(num_frames, dtype=torch.bool)
        for idx in gt_frames:
            if idx < num_frames:
                p_mask[idx] = True
        
        v_mask = torch.ones(num_frames, dtype=torch.bool)
            
        A_list.append(img_embs) # all 50
        B_list.append(q_emb)
        pos_mask_list.append(p_mask)
        valid_mask_list.append(v_mask)

    A_batch = torch.stack(A_list).to(device) # [num_queries, 50, d]
    B_batch = torch.stack(B_list).to(device) # [num_queries, d]
    pos_mask_batch = torch.stack(pos_mask_list).to(device)
    valid_mask_batch = torch.stack(valid_mask_list).to(device)
    
    # 6. Initialize Model & Train
    model = FrameQueryScorer(d=dim, hidden=256).to(device)
    optimizer = optim.AdamW(model.parameters(), lr=5e-4) # Slightly lower LR
    
    epochs = 400
    model.train()
    print("Training FrameQueryScorer...")
    for ep in range(epochs):
        optimizer.zero_grad()
        logits_frames, logit_none = model(A_batch, B_batch, valid_mask=valid_mask_batch)
        loss = set_ce_loss(logits_frames, logit_none, pos_mask_batch, valid_mask=valid_mask_batch)
        loss.backward()
        optimizer.step()
        
        if (ep+1) % 50 == 0:
            print(f"Epoch {ep+1}/{epochs} | Loss: {loss.item():.4f}")
            
    # 7. Save Weights
    save_path = os.path.join(os.path.dirname(__file__), 'method3_trained.pth')
    torch.save(model.state_dict(), save_path)
    print(f"Saved optimal weights to {save_path}")

if __name__ == '__main__':
    main()
