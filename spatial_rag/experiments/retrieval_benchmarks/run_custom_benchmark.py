def compute_metrics(pred_indices, gt_frames):
    '''
    Computes frame-level TP, FP, FN and Jaccard Index.
    Also computes query-level hit: True if at least one predicted frame is in GT.
    '''
    gt_set = set(gt_frames)
    pred_set = set(pred_indices)
    
    tp = len(pred_set.intersection(gt_set))
    fp = len(pred_set - gt_set)
    fn = len(gt_set - pred_set)
    
    if len(gt_set) == 0:
        is_hit = len(pred_set) == 0
    else:
        is_hit = len(pred_set.intersection(gt_set)) > 0
                
    # Jaccard
    intersection = len(pred_set.intersection(gt_set))
    union = len(pred_set.union(gt_set))
    
    if union == 0:
        jaccard = 1.0 # Both empty -> perfect match
    else:
        jaccard = intersection / union
        
    return tp, fp, fn, jaccard, is_hit


def evaluate_method(method, name, queries, image_paths, top_k=5):
    print(f"\n{'='*50}")
    print(f"Evaluating {name} (Top-{top_k})")
    print(f"{'='*50}")
    
    total_tp = 0
    total_fp = 0
    total_fn = 0
    total_hits = 0
    jaccard_scores = []
    
    for item in tqdm(queries, desc=f"Running {name}"):
        query_text = item["query_text"]
        gt_frames = item["gt_frames"] # GT indexes from 0 to 49
        
        # Run method
        result = method.run(query_text, image_paths)
        
        # Extract Top-K
        scores = result.get("all_scores", result.get("all_probs"))
        # Account for method3 array structure
        if "all_probs" in result: 
            frame_scores = scores[:-1]
        else:
            frame_scores = scores
            
        sorted_indices = sorted(range(len(frame_scores)), key=lambda i: frame_scores[i], reverse=True)
        top_k_indices = sorted_indices[:top_k]
        
        pred_is_none = result["is_none"]
        if pred_is_none:
            pred_indices = []
        else:
            pred_indices = top_k_indices
        
        # Compute metrics
        tp, fp, fn, jaccard, is_hit = compute_metrics(pred_indices, gt_frames)
        
        total_tp += tp
        total_fp += fp
        total_fn += fn
        if is_hit:
            total_hits += 1
        jaccard_scores.append(jaccard)
        
        status = "✅ " if is_hit else "❌ "
        pred_str = "None" if pred_is_none else f"Frames {pred_indices}"
        print(f"{status} | Q: '{query_text}' | GT: {len(gt_frames)} frames | Pred: {pred_str} | Jaccard: {jaccard:.2f}")
            
    # Calculate macro F1
    precision = total_tp / (total_tp + total_fp) if (total_tp + total_fp) > 0 else 0
    recall = total_tp / (total_tp + total_fn) if (total_tp + total_fn) > 0 else 0
    f1 = 2 * (precision * recall) / (precision + recall) if (precision + recall) > 0 else 0
    mean_jaccard = sum(jaccard_scores) / len(jaccard_scores) if jaccard_scores else 0
    accuracy = total_hits / len(queries)
    
    return {
        "name": name,
        "tp": total_tp,
        "fp": total_fp,
        "fn": total_fn,
        "f1": f1,
        "mean_jaccard": mean_jaccard,
        "accuracy": accuracy
    }

def main():
    # 1. Load benchmark JSON
    benchmark_file = os.path.join(os.path.dirname(__file__), "autogenerated_benchmark_20.json")
    if not os.path.exists(benchmark_file):
        print(f"Error: Could not find benchmark file {benchmark_file}")
        sys.exit(1)
        
    with open(benchmark_file, "r") as f:
        benchmark_data = json.load(f)
        
    print(f"Loaded {len(benchmark_data)} queries from benchmark.")
    
    # 2. Gather Images
    img_dir = os.path.join(os.path.dirname(__file__), "../../../captured_images")
    image_paths = sorted(glob.glob(os.path.join(os.path.abspath(img_dir), '*.jpg')))
    if not image_paths:
        print(f"No images found in {img_dir}")
        sys.exit(1)
        
    # Benchmark was generated against the first 50 frames
    image_paths = image_paths[:50]
    print(f"Testing against {len(image_paths)} frames...")
    
    # 3. Initialize Shared Embedder
    print(f"\n[System] Loading core Embedder (CLIP)...")
    embedder = Embedder()
    
    # 4. Initialize Methods
    m1 = Method1CLIP(embedder=embedder)
    m2 = Method2VLM(embedder=embedder)
    m3_weights_path = os.path.join(os.path.dirname(__file__), 'method3_trained.pth')
    if not os.path.exists(m3_weights_path):
        print("\nNotice: Method 3 weights not found.")
    m3 = Method3Scorer(embedder=embedder, trained_scorer_path=m3_weights_path)
    
    # 5. Run Evaluations
    res1 = evaluate_method(m1, "Method 1 (CLIP)", benchmark_data, image_paths)
    res2 = evaluate_method(m2, "Method 2 (VLM)", benchmark_data, image_paths)
    res3 = evaluate_method(m3, "Method 3 (Scorer)", benchmark_data, image_paths)
    
    # 6. Summary Output formatted as requested
    print(f"\n{'*'*90}")
    print(f"FINAL BENCHMARK SUMMARY ({len(benchmark_data)} Queries, {len(image_paths)} Frames, Top-5)")
    print(f"{'*'*90}")
    
    print(f"{'Mode':<20} | {'TP':<5} | {'FP':<5} | {'FN':<5} | {'F1 Score':<10} | {'Mean Jaccard':<12} | {'Hit Acc':<10}")
    print("-" * 90)
    
    for r in [res1, res2, res3]:
        print(f"{r['name']:<20} | {r['tp']:<5} | {r['fp']:<5} | {r['fn']:<5} | {r['f1']:<10.3f} | {r['mean_jaccard']:<12.3f} | {r['accuracy']*100:>6.1f}%")

if __name__ == "__main__":
    main()
