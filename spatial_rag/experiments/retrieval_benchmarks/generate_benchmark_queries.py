import os
import sys
import glob
import json
import base64

# Add root to sys path
sys.path.append(os.path.abspath(os.path.join(os.path.dirname(__file__), '../../..')))
import spatial_rag.config # This populates os.environ["OPENAI_API_KEY"]

from openai import OpenAI

# 1. Gather all images
images_dir = os.path.abspath(os.path.join(os.path.dirname(__file__), '../../../captured_images'))
image_paths = sorted(glob.glob(os.path.join(images_dir, '*.jpg')))

if not image_paths:
    print(f"No images found in {images_dir}.")
    exit()

print(f"Found {len(image_paths)} images. Using the first 50 frames to generate the benchmark.")
image_paths = image_paths[:50]

client = OpenAI(api_key=os.environ.get("OPENAI_API_KEY"))

# 2. Use VLM to extract key objects from a sampled set of frames to build a vocabulary
sampled_paths = image_paths[::5] # Sample every 5th frame for context
encoded_images = []
for p in sampled_paths:
    with open(p, "rb") as image_file:
        encoded_images.append(base64.b64encode(image_file.read()).decode('utf-8'))

print("Analyzing scene context to generate realistic queries...")

content_list = [{"type": "text", "text": "These are frames from a video tour of a room. Based on these images, generate exactly 30 diverse query questions asking to find specific objects or spatial arrangements (e.g. 'a large wooden chair', 'a painting of a horse', 'a chandelier hanging from the ceiling'). Also estimate the frame indices (0-49) where these objects likely appear based on the progression of the video. Format your response strictly as a JSON list of dictionaries with keys: 'query_text' and 'gt_frames' (a list of integers). Do not wrap in markdown blocks."}]
for enc in encoded_images:
    content_list.append({"type": "image_url", "image_url": {"url": f"data:image/jpeg;base64,{enc}", "detail": "low"}})

try:
    response = client.chat.completions.create(
        model="gpt-4o", # Use the smarter model to write the benchmark
        messages=[{"role": "user", "content": content_list}],
        max_tokens=1500,
        temperature=0.2
    )
    
    json_str = response.choices[0].message.content.strip()
    # clean up potential markdown formatting
    if json_str.startswith("```json"):
        json_str = json_str[7:-3].strip()
        
    benchmark_data = json.loads(json_str)
    
    # Force add ID and standardize
    for i, task in enumerate(benchmark_data):
        task["query_id"] = f"q{i+1}"
        task["video_idx"] = 0 # All from the first 50 frames
        
    # Save to disk
    out_path = os.path.join(os.path.dirname(__file__), "autogenerated_benchmark_30.json")
    with open(out_path, "w") as f:
        json.dump(benchmark_data, f, indent=4)
        
    print(f"\n✅ Automatically Generated 30 Benchmark Queries saved to: {out_path}")
    for t in benchmark_data[:5]:
        print(f" - {t['query_id']}: '{t['query_text']}' -> GT: {t['gt_frames']}")
    print("...")

except Exception as e:
    print(f"Error generating benchmark: {e}")
